using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using Project_Vacation_Manager.Data;
using Project_Vacation_Manager.Models;

namespace Project_Vacation_Manager.Controllers
{
    public class ProjectsController : Controller
    {
        private readonly Project_Vacation_ManagerContext _context;

        public ProjectsController(Project_Vacation_ManagerContext context)
        {
            _context = context;
        }

        // GET: Projects
        public async Task<IActionResult> Index()
        {

            return View(await _context.Project.ToListAsync());
        }

        // GET: Projects/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var project = await _context.Project
                .FirstOrDefaultAsync(m => m.Id == id);
            project.Teams = _context.Team.Where(x => x.Project == project.Name).ToList();
          
            if (project == null)
            {
                return NotFound();
            }

            return View(project);
        }

        // GET: Projects/Create
        [Authorize(Roles = "CEO")]
        public IActionResult Create()
        {
            return View();
        }

        [Authorize(Roles = "CEO")]
        public async Task<RedirectToActionResult> DeleteTeamAsync(Team teamm)
        {
            //var teamm = _context.Team[id];
        //    teamm.Project = "";
            
